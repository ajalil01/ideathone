function display1(){
    let d1 = document.getElementById("des-1")
    d1.style.display = 'block';
    let h1 = document.getElementById("hide-1")
    h1.style.display = 'flex'
    let b1 = document.getElementById("display-1")
    b1.style.display = 'none'
}

function hide1(){
    let d1 = document.getElementById("des-1")
    d1.style.display = 'none';
    let h1 = document.getElementById("hide-1")
    h1.style.display = 'none'
    let b1 = document.getElementById("display-1")
    b1.style.display = 'block'
}

